<h1>Prueba Nivelación de Inglés:</h1>

Es necesario que todos los alumnos presenten una prueba de nivelación de inglés antes de inscribirse en nuestros cursos.  Esta prueba es gratuita y se presenta en las instalaciones de CSA TEST PREP. El resultado que el estudiante obtenga en esta prueba nos indicara su dominio del idioma y nos permitirá ubicarlo en el curso que cumpla con su nivel.  Una vez sepamos el nivel del estudiante podemos indicarle la trayectoria y próximos pasos a seguir para lograr el mejor resultado posible en el proceso de admisión a las universidades.

