				El GMAT (Graduate Management Admission Test) es un examen estandarizado que tiene como objetivo principal medir las habilidades matemáticas, verbales y analíticas de profesionales aspirantes a cursar una formación en gestión de empresas.
				<br><br>
				El GMAT es obligatorio para ser admitido a los MBA (Master in Business Administration) de las universidades y escuelas de negocios más prestigiosas del mundo y representa uno de los criterios de selección más importantes para los aspirantes a estos programas.
				<br><br>
				Al igual que el TOEFL, el GMAT es un examen computarizado que se adapta al que lo está tomando. Cada sección de respuesta múltiple comprende un amplio rango de preguntas posibles que van desde el nivel más sencillo hasta el más complicado. Al comienzo, las secciones plantean una dificultad moderada y el grado de acierto del participante determinará el nivel de dificultad de la siguiente. Lo que este sistema implica es que cada examen GMAT realizado es único y personal para cada participante.
				<br><br>
				La totalidad del examen se realiza en inglés y los resultados del mismo se envían tanto al candidato como a las escuelas de negocio seleccionadas por éste. La validez del examen es de cinco años, contados desde la fecha en que se presentó. La puntuación combina los resultados de la sección analítica, la sección cuantitativa y la sección verbal. El rango de puntuación del GMAT oscila entre los 200 y 800 puntos.

				<h1>Estructura GMAT:</h1>

				1. Introducción/ Tutorial
				<br><br>
				2. Sección Analítica (60min)
				- Redacción 1: Análisis de un argumento (Analysis of an Argument)
				- Redacción 2: Análisis de un tema (Analysis of an Issue)
				<br><br>
				3. Sección Cuantitativa (75 min)
				- Problemas Matemáticos (Problem Solving)
				- Suficiencia de información para resolver problemas matemáticos (Data Sufficiency)
				<br><br>
				4. Sección Verbal (75min)
				- Comprensión y razonamiento sobre temas escritos (Critical Reasoning)
				- Comprensión lectora (Reading Comprehension)
				- Corrección de texto en oraciones (Sentence Correction)

