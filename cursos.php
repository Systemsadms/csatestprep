		<br><br>
		Duración: 50 Hrs. Académicas.

		<h1>Curso de SAT:</h1>

		El curso está diseñado para garantizar un buen manejo de la estructura, formato y contenido del examen. Los alumnos que se inscriban en este curso aprenderán estrategias y obtendrán las herramientas necesarias para lograr la mejor calificación posible. Los alumnos completarán una revisión de las 3 secciones del examen: Escritura, Lectura y Matemática, y practicaran todos los tipos y formatos de preguntas. También, se realizaran diagnósticos y prácticas por medio de exámenes reales de años anteriores.
		<br><br>
		Duración: 80 Hrs. Académicas.

