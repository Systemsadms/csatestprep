			<h1>Paquete # 1: ¿Cómo aplicar a las Universidades en el Exterior?</h1>

Aplicar a las Universidades americanas no es tan fácil como parece. Las instituciones exigen amplio papeleo con información personal, académica y económica en cada caso.  Para facilitar este proceso, ofrecemos reuniones con nuestros asesores donde se les explicará al estudiante y a su representante la manera efectiva de aplicar a las universidades.
<br><br>

Preguntas a responder: ¿Qué se necesita para aplicar a las universidades?, ¿Cómo se consigue cada documento?, ¿Cómo se arman los paquetes con la información que las universidades exigen?, ¿Qué información debe ir dentro de estos paquetes?, ¿Que se le envía por correo y online a cada universidad?, ¿Como se le comprueba al Estado Americano que se pueda pagar la universidad de forma que la universidad mande el i20 para pedir la cita a la embajada?

<h1>Paquete # 2: Búsqueda de Universidades.</h1>

Nuestros asesores les harán un cuestionario al estudiante y a su representante. Este se utilizará para buscar todas las universidades en que estén dentro de la zona, precio y cuenten con la carrera deseada. Como valor agregado le informaremos la opción de beca que tendría el estudiante en cada institución. Para finalizar, se le enviará un cuadro con los siguientes aspectos de cada universidad: 1. País, Ciudad y Estado, 2. Precio, 3. Opción de Beca, 4. Porcentaje de Aceptación, 5. Tier o Rango de la Universidad.

<h1>Paquete # 3: Llenar las aplicaciones.</h1>

El estudiante se reunirá con el asesor para llenar sus aplicaciones de cada institución, asegurándose así de que la información enviada a la universidad es la correcta y va de la mano con lo que cada institución busca para conseguir que el estudiante tenga buenas posibilidades de ingresar a la universidad. Este paquete incluye la corrección de los ensayos requeridos por cada institución y ayuda en el proceso de entrevista (si la universidad lo requiere).

<h1>Paquete # 4: Proceso de la Visa Americana</h1>

Se llenarán la planillas requeridas por la embajada junto a un asesor académico y se dará información sobre el material que debe llevar el día de la cita para asegurar de esta forma que al estudiante le sea otorgada la visa americana.
